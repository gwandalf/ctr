package interfaces;

/**
 * 2D point
 * @author gwlemoul
 *
 */
public interface PointInterface {

	/**
	 * Getter
	 * @return x
	 */
	double getX();
	
	/**
	 * Setter
	 * @param x
	 */
	void setX(double x);
	
	/**
	 * Getter
	 * @return y
	 */
	double getY();
	
	/**
	 * Setter
	 * @param y
	 */
	void setY(double y);
	
}
