package interfaces;

/**
 * 2D point
 * @author gwlemoul
 *
 */
public interface PointInterface {

	/**
	 * Getter
	 * @return x
	 */
	int getX();
	
	/**
	 * Setter
	 * @param x
	 */
	void setX(int x);
	
	/**
	 * Getter
	 * @return y
	 */
	int getY();
	
	/**
	 * Setter
	 * @param y
	 */
	void setY(int y);
	
}
